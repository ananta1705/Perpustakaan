    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
     try {
    stat.executeUpdate("insert into perpus values ("
    + "'" + txtNo.getText()+"',"
    + "'" + txtJudul.getText()+"',"
    + "'" + txtPengarang.getText()+ "',"
    + "'" + txtTahun.getText()+ "',"
    + "'" + txtPenerbit.getText()+ "',"
    );
    kosongkan();//Mengosongkan text field setelah berhasil menyimpan data
    tabel();// Menampilkan isi tabel
    JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan...");
    } catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Keterangan Error : "+e);
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnKeluarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKeluarActionPerformed
        try {
//melakukan pencarian data berdasarkan primary key pada tabel
res=stat.executeQuery("select * from perpus where "+ "no_buku='"+txtNo.getText()+"'"
);

while (res.next()) {
txtJudul.setText(res.getString("judul_buku"));
txtPengarang.setText(res.getString("pengarang"));
txtTahun.setText(res.getString("tahun"));
txtPenerbit.setText(res.getString("penerbit"));
}
} catch (Exception e) {
JOptionPane.showMessageDialog(rootPane, e);
}
    }//GEN-LAST:event_btnKeluarActionPerformed

